            <h4 class="font-weight-bold text-primary mb-3">
                Manuscript Preparation Guidelines
            </h4>

            <h5 class="font-weight-bold mt-4">Title</h5>
            <p class="text-justify">
                The title should briefly describe the contents of the paper. The title page should include authors’ full names, affiliations, corresponding author details, contact number, and email address.
            </p>

            <h5 class="font-weight-bold mt-4">Abstract</h5>
            <p class="text-justify">
                The abstract should be informative, self-explanatory, and within 250 words. It should briefly present the topic, methods, findings, and conclusions. Avoid abbreviations and literature citations.
            </p>

            <h5 class="font-weight-bold mt-4">Keywords</h5>
            <p class="text-justify">
                Include approximately 6 keywords for indexing references.
            </p>

            <h5 class="font-weight-bold mt-4">Introduction</h5>
            <p class="text-justify">
                The introduction should be concise and define the scope of the work in relation to previous studies.
            </p>

            <h5 class="font-weight-bold mt-4">Materials and Methods</h5>
            <p class="text-justify">
                Clearly describe the experiments and techniques used. Previously published procedures should be cited, and only new methods should be described in detail.
            </p>

            <h5 class="font-weight-bold mt-4">Statistical Methods</h5>
            <p class="text-justify">
                Mention statistical tests used and levels of significance. Clearly indicate which groups and parameters were subjected to specific tests.
            </p>

            <h5 class="font-weight-bold mt-4">Results</h5>
            <p class="text-justify">
                Present results clearly and precisely. Use past tense when describing findings from the study.
            </p>

            <h5 class="font-weight-bold mt-4">Discussion</h5>
            <p class="text-justify">
                The Discussion section should interpret the findings in relation to the results obtained in the present study as well as previous studies on the topic.
            </p>

            <p class="text-justify">
                This section should focus on interpretation rather than repetition of results. Authors should discuss significant and new observations in the context of earlier work. Weaknesses or limitations of the study should also be highlighted. New hypotheses or recommendations may be proposed where appropriate. Avoid unsupported conclusions or unqualified statements. Repetition of information already presented in the Introduction and Results sections should be avoided.
            </p>

            <h4 class="font-weight-bold text-primary mt-5 mb-3">
                Conclusion
            </h4>

            <p class="text-justify">
                State the conclusions clearly and concisely at the end of the paper. Conclusions should be drawn considering both the strengths and limitations of the study.
            </p>

            <!-- Tables -->
            <h4 class="font-weight-bold text-primary mt-5 mb-3">
                Tables
            </h4>

            <p class="text-justify">
                Tables should be self-explanatory, simple, and kept to a minimum. They should be placed appropriately within the text at the location where they are first mentioned. Experimental method details should preferably be included in table legends rather than repeated in the main text.
            </p>

            <p class="text-justify">
                The same data should not be presented in both table and graphical/pictorial forms.
            </p>

            <p class="text-justify">
                Tables and figures should not be placed only at the end of the article. They must appear within the text at suitable positions where they are referenced.
            </p>

            <!-- Figure Legends -->
            <h4 class="font-weight-bold text-primary mt-5 mb-3">
                Figure Legends
            </h4>

            <p class="text-justify">
                Graphics should be prepared using applications capable of generating high-resolution GIF, TIFF, JPEG, or PowerPoint files before inserting them into the Microsoft Word manuscript. Use Arabic numerals for figure numbering and uppercase letters for figure parts (e.g., Figure 1). Figures may also be attached separately as JPEG files.
            </p>

            <!-- References -->
            <h4 class="font-weight-bold text-primary mt-5 mb-3">
                References
            </h4>

            <p class="text-justify">
                References should follow the Vancouver citation style.
            </p>

            <p class="text-justify">
                Avoid citing abstracts as references.
            </p>

            <p class="text-justify">
                Each reference should be assigned a consecutive number according to the order in which it first appears in the text.
            </p>

            <p class="text-justify">
                References should be cited in the text using superscripted numbers in sequential order. The same reference number should be reused whenever the same source is cited again. Multiple references should be represented using commas and hyphens where appropriate (e.g., 2-5, 7, 10).
            </p>

            <p class="text-justify">
                In Vancouver style, reference numbering should continue in increasing order throughout the manuscript beginning from the Introduction section. Reference numbers should not be used in the Abstract section.
            </p>

            <p class="text-justify mb-0">
                The reference list should appear at the end of the paper. If there are three or fewer authors, list all authors. If there are more than three authors, use “et al.” after the third author.
